class Solution(object):
    def countPrefixes(self, words, s):
        """
        :type words: List[str]
        :type s: str
        :rtype: int
        """
        res = 0
        for i in range(len(words)):
            if words[i] == s[:len(words[i])]:
                res += 1
        
        return res 
