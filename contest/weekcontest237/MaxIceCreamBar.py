class Solution(object):
    def maxIceCream(self, costs, coins):
        """
        :type costs: List[int]
        :type coins: int
        :rtype: int
        """
        costs.sort()
        res = 0
        for i, c in enumerate(costs):
            if c <= coins:
                res += 1
                coins -= c
            else:
                break
        return res 
