class Solution(object):
    def targetIndices(self, nums, target):
        """
        :type nums: List[int]
        :type target: int
        :rtype: List[int]
        """
        res = []
        nums.sort()
        for i in range(len(nums)):
            if nums[i] > target:
                break
            if target == nums[i]:
                res.append(i)
        return res
