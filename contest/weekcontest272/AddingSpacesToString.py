class Solution(object):
    def addSpaces(self, s, spaces):
        """
        :type s: str
        :type spaces: List[int]
        :rtype: str
        """
        l = list(s) 
        for i in spaces: 
            l[i] = " " + l[i] 
        return ''.join(l) 
